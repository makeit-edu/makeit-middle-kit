#!/usr/bin/env bash
# ============================================================================
# 월부 중급반 키트 — 접속할 때마다 실행되는 환영 안내 (PRD 4-4, 8-R3)
# (devcontainer.json 의 postAttachCommand 가 호출)
#
# 역할: ① 오늘 할 일(다음 명령) 안내  ② 커밋 안 된 작업물 경고
# 어떤 경우에도 오류로 끝나지 않도록 마지막에 exit 0 고정.
# ============================================================================

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
PROGRAM_DIR="$ROOT/99_절대_건들지마세요_프로그램파일"

echo ""
echo "=============================================="
echo "  📦 월부 중급반 키트에 오신 것을 환영합니다!"
echo "=============================================="
echo ""

# ── 자동 환경 점검: 수강생이 '진단'을 입력할 필요 없이 접속 때마다 조용히 확인 ──
# 통과하면 한 줄만, 문제가 있으면 [확인 필요] 항목과 다음 행동을 보여준다.
DOCTOR_OUT="$(cd "$PROGRAM_DIR" 2>/dev/null && node scripts/doctor.mjs 2>/dev/null)"
if [ $? -eq 0 ]; then
  echo "  ✅ 환경 점검 통과 — 작업할 준비가 됐어요."
else
  echo "  ⚠️  환경 점검에서 아래 항목이 걸렸어요:"
  echo "$DOCTOR_OUT" | grep "확인 필요" | sed 's/^/     /'
  echo ""
  echo "     터미널에 '진단' 을 입력해 나온 결과 전체를 복사해서 문의 채널에 올려 주세요."
fi
echo ""

# ── 다음 할 일 안내: 키설정 완료 여부(수강 코드 입력 흔적)로 분기 ──────────
# README 온보딩(3단계 접속 확인 → 4단계 로그인 → 5단계 키설정 → 6단계 시작)과 같은 순서로 안내한다.
if ! grep -qE '^MAKEIT_MIDDLE_LICENSE=..+' "$PROGRAM_DIR/.env.local" 2>/dev/null; then
  echo "  처음이시죠? 아래 순서대로 터미널에 입력하면 됩니다. (README 1장과 같은 순서)"
  echo ""
  echo "    1) 키설정   ← API 키 2개 + 수강 코드를 넣는 단계"
  echo "    2) 시작     ← AI 비서(코덱스)를 켜는 단계 (첫 사용 시 로그인 안내가 자동으로 나와요)"
  echo ""
else
  echo "  준비 완료 상태예요. 터미널에 '시작' 을 입력하면 바로 작업할 수 있어요."
  echo ""
fi

echo "  ── 쓸 수 있는 한글 명령 ──────────────────"
echo "    시작      AI 비서(코덱스) 켜기"
echo "    진단      뭔가 이상할 때 상태 점검"
echo "    키설정    API 키·수강 코드 입력/변경"
echo "    저장      작업물 안전하게 보관(커밋)"
echo "    정리      용량 확보(완료된 작업 청소)"
echo "    업데이트  프로그램 최신판 받기"
echo "  ── 2주차 영상 전용 ───────────────────────"
echo "    새상품 1     상품 작업 만들기 (번호 필수)"
echo "    이미지만들기  기획안 기준 이미지 생성"
echo "    영상만들기    음성 + 영상 렌더링"
echo "    편집기        영상 편집기 켜기 (포트 4321)"
echo "    내보내기      완성영상 zip 묶음"
echo "  ──────────────────────────────────────────"
echo ""
echo "  ※ 명령이 안 먹으면 터미널을 새로 열어 주세요. (+ 버튼)"

# ── 커밋 안 된 작업물 경고 (30일 미접속 삭제 대비 안전망, 8-R3) ────────────
if git -C "$ROOT" status --porcelain 2>/dev/null | grep -q .; then
  echo ""
  echo "  ⚠️  아직 저장(커밋)하지 않은 작업물이 있어요!"
  echo "      이 화면을 오래 안 열면 작업물이 사라질 수 있으니,"
  echo "      작업이 끝나면 터미널에 '저장' 을 입력해 주세요."
fi

echo ""
exit 0
